public class Swap {
    public static void main(String[] args) {

        int x=2,y=6,t;
        System.out.println("First number = " + x);
        System.out.println("Second number = " + y);
        t=x;
        x=y;
        y=t;
        System.out.println("First number = " + x);
        System.out.println("Second number = " +y);
    }
}
